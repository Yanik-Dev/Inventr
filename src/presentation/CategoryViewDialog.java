    public void init(){
        ((DefaultTableModel) this.tableCategories.getModel()).setNumRows(0);
        this._dbService = new CategoryService();
        DefaultTableModel model = (DefaultTableModel) this.tableCategories.getModel();
        model.setRowCount(0);
        this.categories = this._dbService.findAll(Category.class);
        for(Category category : categories){ 
            model.addRow(new Object[]{
                                       category.getCategoryId(), 
                                       category.getName()
                                     });
        }
    }
    
